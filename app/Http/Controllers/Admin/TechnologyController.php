    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $technologies = Technology::orderBy('id', 'desc')->paginate(5);
        return view('admin.technologies.index', compact('technologies'));
    }

